def p(x):
    return np.exp(-x)/(1+np.exp(-x))

def integrand(tau):
    x = x_max * tau
    p_tau = p(x)

    dot_x = x_max  # derivative of x with respect to tau (RW linear case)

    return (dot_x**2) * p_tau - (p_tau * dot_x)**2

def W_diss(N):
    taus = np.linspace(0,1,5000)
    integral = np.trapz(integrand(taus), taus)
    return (1/(2*N)) * integral

