import numpy as np
import matplotlib.pyplot as plt

beta = 1e-8
lam = 1e9
E_S = 1.0
x_max = beta * lam

def p(x):
    return np.exp(-x) / (1 + np.exp(-x))

def entropy(x):
    p0 = p(x)
    eps = 1e-15
    p0 = np.clip(p0, eps, 1-eps)
    return -p0*np.log(p0) - (1-p0)*np.log(1-p0)

def relative_entropy(x1, x2):
    p1 = p(x1)
    p2 = p(x2)

    return p1*np.log(p1/p2) + (1-p1)*np.log((1-p1)/(1-p2))


def beta_delta_E(N):
    if N == 0:
        return 0.0

    x_vals = np.linspace(0, x_max, N+1)
    total = 0.0
    for j in range(N):
        total += relative_entropy(x_vals[j], x_vals[j+1])

    return total

